export const getAllCategory = () => {
    return fetch(`${process.env.NEXT_PUBLIC_API}/categories`, {
        method: 'GET',
        headers: {
            Accept: 'application/json',
            'Content-Type': 'application/json'
        }
    })
        .then(response => {
            return response.json();
        })
        .catch(err => console.log(err));
};

